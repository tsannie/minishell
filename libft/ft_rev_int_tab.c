void	ft_rev_int_tab(int *tab, int size)
{
	int b0;
	int b1;
	int save;

	b0 = 0;
	b1 = size - 1;
	while (b0 < b1)
	{
		save = tab[b0];
		tab[b0] = tab[b1];
		tab[b1] = save;
		b0++;
		b1--;
	}
}
